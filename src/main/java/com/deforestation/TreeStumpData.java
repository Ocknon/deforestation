package com.deforestation;

public class TreeStumpData
{
    public int treeId;
    public int stumpId;
    public int orientation;

    public TreeStumpData(int treeId, int stumpId, int orientation)
    {
        this.treeId = treeId;
        this.stumpId = stumpId;
        this.orientation = orientation;
    }
}
